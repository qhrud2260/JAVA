package p47;

public class FloatDoubleExample {
	public static void main(String[] args) {
		double var1 = 3.14;
		float var3 = 3.14F;
		
		double var4 = 0.123456789123456789;
		float var5 = 0.123456789123456789F;
		
		System.out.println("var1: "+var1);
		System.out.println("var1: "+var3);
		System.out.println("var1: "+var4);
		System.out.println("var1: "+var5);
		
		int var6 = 3000000;
		double var7 = 3e6;
		float var8 = 3e6F;
		double var9 = 2e-3;
		
		System.out.println("var1: "+var6);
		System.out.println("var1: "+var7);
		System.out.println("var1: "+var8);
		System.out.println("var1: "+var9);
	}
}
