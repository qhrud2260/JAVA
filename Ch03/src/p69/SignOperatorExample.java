package p69;

public class SignOperatorExample {

	public static void main(String[] args) {
		int x = -100;
		int result1 = +x;
		int result2 = -x;
		System.out.println("result = " + result1);
		System.out.println("result2 = " + result2);
		
		int result3 = -5;
		System.out.println("result3 = "+result3);
	}

}
